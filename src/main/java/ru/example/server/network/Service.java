/**
 * @author TaylakovSA
 */
public class Service {

    private String name;
    private String host;
    private String port;
    private String version;

    public String getName() {
        return name;
    }

    public String getHost() {
        return host;
    }

    public String getPort() {
        return port;
    }

    public String getVersion() {
        return version;
    }
}
